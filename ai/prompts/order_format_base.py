def expand_user(current_text: str) -> str:
    return (
        "Сделай описание более подробным и развёрнутым. "
        "Расширь раздел с задачами, добавь детали в описание. "
        "Сохрани структуру, стиль и все эмодзи.\n\n"
        f"Текущий текст:\n{current_text}"
    )


def shorten_user(current_text: str) -> str:
    return (
        "Сделай описание более кратким. "
        "Сократи описание задачи и список задач до самого главного. "
        "Сохрани структуру, стиль и все эмодзи.\n\n"
        f"Текущий текст:\n{current_text}"
    )


